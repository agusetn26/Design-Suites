        <div class="col-md-6">
            <h4>5. Desinfección de superficies y ventilación de ambientes:</h4>
            <p>
                Se realiza regularmente, con mayor frecuencia en las áreas de alto tránsito y mayor acumulación de
                personas. </p>
            <p>
                Para la limpieza de los sectores claves, como la Recepción, ascensor y manijas de puertas utilizamos
                productos que garanticen la higiene y correspondiente desinfección. </p>
            <p>
                Mantenemos la limpieza y desinfección de áreas de trabajo durante la jornada laboral, prestando mayor
                atención a los elementos de uso frecuente, tales como teléfono, computadora, lapiceras, etc. </p>
        </div>
    </div>
    <p></p>


