    <h1>PROTOCOLO COVID-19 DESIGN SUITES</h1>
    Estimados huéspedes,
    </p>
    <p>
        Design Suites ha elaborado el Protocolo COVID-19 para la reapertura de los Hoteles de la Cadena regido por las
        recomendaciones del Ministerio de Turismo y Deportes de la Nación en conjunto con el Instituto de Calidad
        Turística Argentina (ICTA), la Cámara Argentina de Turismo (CAT), la Asociación de Hoteles de Turismo (AHT), la
        Asociación de Hoteles, Restaurantes, Confiterías y Cafés (AHRCC), la Federación Empresaria Hotelera Gastronómica
        de la República Argentina (FEHGRA), y de las provincias a través del Consejo Federal de Turismo.
    </p>
    <p>
        Las pautas contenidas en el presente protocolo siguen las recomendaciones establecidas por el Ministerio de
        Salud de la Nación, siendo su principal objetivo resguardar la salud y bienestar de trabajadores, huéspedes y
        turistas.
    </p>
    <p>
        Por esta razón hemos adaptado los espacios y servicios para que la estadía resulte segura y placentera.
    </p>
    <p>
        Remarcamos que las recomendaciones y sugerencias contenidas en este documento son meramente guías para prácticas
        dentro de la industria. Estas pautas no están destinadas a reemplazar los procedimientos de saneamiento, leyes y
        licencias existentes, requisitos o regulaciones establecidas por el gobierno nacional y de cada provincia.
    </p>
    <p>
        ¡Esperamos con ansias el reencuentro, estamos felices de recibirlos!
    </p>
