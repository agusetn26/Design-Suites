        <div class="col-md-6">
            <h4>4. Higiene respiratoria:</h4>
            <p>
                La utilización del tapabocas y barbijo es obligatoria en todas las áreas del hotel, tanto para huéspedes como empleados. El mismo debe cubrir completamente la boca, nariz y mentón, y lavarse diariamente. Para completar cuidados hemos instalado divisores/mamparas como medida física de protección. </p>
        </div>
    </div>
