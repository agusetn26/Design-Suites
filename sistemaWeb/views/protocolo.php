            <p>
                6. El servicio de mesa (utensilios, platos, vasos, etc.) se ofrece una vez que el comensal se encuentre ubicado en su lugar de reserva nunca antes de su llegada.
            <p>
                7. Ofrecemos las bebidas cerradas en botella o lata.
