        </div>
        <div class="col-md-6">
            <h4>2. Distanciamiento social:</h4>
            <p>
                Respetamos una distancia mínima de 1.5/2 metros tanto entre empleados, como entre huéspedes y clientes.
                Es indispensable evitar el contacto físico al saludar. Limitamos la densidad de ocupación de espacios
                utilizando la modalidad de reserva o de turnos prefijados. Los espacios destinados a la espera de turnos
                se encuentran señalizados para una mejor organización. </p>
        </div>
    </div>
    <p></p>
    <!--3-->
    <div class="row">
        <div class="col-md-5">
            <img src="https://www.designsuites.com/images/protocolo_3.jpg" alt="Imagen" class="img-fluid" style="max-width: 100%;">
        </div>
        <div class="col-md-6">
            <h4>3. Higiene de manos:</h4>
            <p>
                Recomendamos el lavado de manos frecuente, con una duración mínima de 40/60 segundos. </p>
            <p>
                Disponemos de alcohol en gel tanto para el consumo de huéspedes como para empleados. </p>
            <p>
                La utilización de guantes de látex se recomienda exclusivamente para tareas de limpieza y deben ser
                utilizados a consciencia. </p>
        </div>
    </div>
    <p></p>
    <!--4-->
    <div class="row">
        <div class="col-md-5">
            <img src="https://www.designsuites.com/images/protocolo_4.jpg" alt="Imagen" class="img-fluid" style="max-width: 100%;">
        </div>
        <div class="col-md-6">
            <h4>4. Higiene respiratoria:</h4>
            <p>
                La utilización del tapabocas y barbijo es obligatoria en todas las áreas del hotel, tanto para huéspedes
                como empleados. El mismo debe cubrir completamente la boca, nariz y mentón, y lavarse diariamente. Para
                completar cuidados hemos instalado divisores/mamparas como medida física de protección. </p>
        </div>
    </div>
    <p></p>
