                2. En caso de espera, procuramos disponer de áreas para hacer su Check In o Check Out. </p>
            <p>
                2. Design Suites brinda el servicio de desayuno en el restaurante con previa reserva, sugerimos el servicio en la habitación y Take Away.
            <p>
                4. Con anticipación hacemos llegar por mail u otro medio electrónico la ficha de registro habilitando el web Check In el día previo a la llegada, para que el proceso resulte más rápido y evitando la exposición tanto del personal como huéspedes.
            <p>
                3. La reserva previa de horario nos permite planificar la cantidad de personas desde el día anterior, estableciéndose un tiempo prudente del consumo del servicio para cada turno y el cumplimiento del distanciamiento social para extremar cuidados.
            <p>
                4. Damos prioridad a la utilización de servilletas e Individuales de papel descartable (Por encima de la superficie de las mesas).
            <p>
