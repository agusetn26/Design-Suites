    <div class="row">
        <h3>Detección y manejo de casos sospechosos</h3>
        <div class="col-md-6">
            <img src="../img/home_calafate.jpg" alt="Imagen" class="img-fluid" style="max-width: 100%;">
        </div>
        <div class="col-md-6">
            <p>
                1. Orientamos actividades de identificación de potenciales casos fomentando el auto reporte y la realización de cuestionarios sencillos para la detección de posibles síntomas. </p>
            <p>
                2. Disponemos de termómetro infrarrojo para la medición de temperatura a toda persona que ingrese al establecimiento.
            </p>
            <p>
                3. Por razones de prevención social no permitimos el acceso a personas con temperatura mayor o igual a 37.5 o que presente síntomas compatibles con COVID-19.
            </p>
            <p>
                4. Ante la identificación de personas con síntomas respiratorios o fiebre, se contactará inmediatamente al Sistema de Emergencias de salud local para su evaluación y eventual trasladado a una institución de salud. Notificaremos toda sospecha de COVID-19 a la autoridad sanitaria local, de acuerdo con el protocolo nacional.
            </p>
        </div>
    </div>
    <p></p>

    <div class="row">
        <h3>Capacitación del personal</h3>
        <div class="col-md-6">
            <img src="../img/home_calafate.jpg" alt="Imagen" class="img-fluid" style="max-width: 100%;">
        </div>
        <div class="col-md-6">
            <p>
                Informamos y capacitamos a los empleados en reconocimiento de síntomas de la enfermedad y de las medidas de prevención de COVID-19 de acuerdo con la información oficial que comunica el Ministerio de Salud de la Nación. </p>
            <p>
                El personal de la empresa permanece organizado en equipos para cumplir con el distanciamiento establecido. Para los casos en los que no fue posible, se extremaron las medidas de protección sanitarias.
            </p>
            <p>
                Se realizan los mejores esfuerzos para garantizar la mínima convivencia simultánea de personas en un mismo espacio físico cerrado.
        </div>
    </div>
    <p></p>

    <div class="row">
        <h3>Áreas Públicas </h3>
        <div class="col-md-6">
            <img src="../img/home_calafate.jpg" alt="Imagen" class="img-fluid" style="max-width: 100%;">
        </div>
        <div class="col-md-6">
            <p>
                1. En ascensores sugerimos el uso individual o bien no ocupar más del 30% de la superficie de la cabina, recomendamos no compartir el mismo con otros huéspedes, salvo en el caso de grupo familiar.
            <p>
                2. Disponemos de alcohol en gel en todos los espacios comunes, especialmente aquellos de mayor circulación, también la provisión de agua y jabón en sanitarios, asegurando su reposición. </p>
            <p>
                3. Señalizamos claramente el bloqueo o el acceso a las áreas que se encuentren restringidas.
            <p>
                4. Sugerimos a los huéspedes evitar circular innecesariamente por las áreas públicas.
            </p>
            <p>
                5. Eliminamos objetos de contacto en todos los espacios comunes (folletería, diarios, revistas).
            </p>
            <p>
                6. Piscina, spa y gimnasio, el uso (con manejo de turnos) o restricción de dichas áreas, depende de las medidas sugeridas por las autoridades sanitarias de cada provincia y/o jurisdicción correspondiente.
            </p>
            <p>
                7. Barras, mostradores y similares: Adaptamos al uso público o restricción según las normas aplicadas por la autoridad sanitaria en cada destino.
        </div>
    </div>
    <p></p>

    <div class="row">
        <h3>Recepción</h3>
        <div class="col-md-6">
            <img src="../img/home_calafate.jpg" alt="Imagen" class="img-fluid" style="max-width: 100%;">
        </div>
        <div class="col-md-6">
            <p>
                1. Al momento de realizar la reserva, informamos las medidas preventivas que aplicamos para preservar la seguridad y salud. Junto a la confirmación enviamos el protocolo de nuestra cadena.
            <p>
                2. En caso de espera, procuramos disponer de áreas para hacer su Check In o Check Out. </p>
            <p>
                3. Incentivamos el uso de medios de pago digitales.
            <p>
                4. Con anticipación hacemos llegar por mail u otro medio electrónico la ficha de registro habilitando el web Check In el día previo a la llegada, para que el proceso resulte más rápido y evitando la exposición tanto del personal como huéspedes.

            <p>
                5. Para la detección de síntomas e identificación de posibles casos COVID-19 realizamos en el momento del Chec
            <p>
                kin un cuestionario y es obligatoria la firma de la declaración jurada por parte del huésped, dejando constancia si tiene o tuvo síntomas COVID-19, y en qué país o países permaneció los últimos 30 días.

            <p>
                6. En toda ocasión reducimos la utilización e intercambio de documentos u objetos.

            <p>
                7. Utilizamos buzón o urna para el depósito de las llaves o tarjetas magnéticas al finalizar la estadía, para su desinfección previa a su reutilización.

            <p>
                8. Evitamos el traslado de equipaje a las habitaciones para disminuir la exposición tanto de los huéspedes como el personal, siempre y cuando el pasajero no esté imposibilitado de hacerlo.
        </div>
